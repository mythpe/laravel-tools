namespace Myth\LaravelTools\Http\Middleware;

use Closure;
use Illuminate\Http\Request;

class MythDebugMiddleware
{
    /**
     * @param Request $request
     * @param Closure $next
     *
     * @return mixed
     */
    public function handle(Request $request, Closure $next)
    {
        if (developmentMode()) {
            mythAllowHeaders();
        }
        return $next($request);
    }
}
